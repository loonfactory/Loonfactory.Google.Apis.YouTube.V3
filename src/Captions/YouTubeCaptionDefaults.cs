namespace Loonfactory.Google.Apis.YouTube.V3.Captions;

public class YouTubeCaptionDefaults
{
    /// <summary>
    /// Endpoint URL for listing captions.
    /// </summary>
    public static readonly string ListEndpoint = "https://www.googleapis.com/youtube/v3/captions";

    /// <summary>
    /// Endpoint URL for inserting new captions.
    /// </summary>
    public static readonly string InsertEndpoint = "https://www.googleapis.com/upload/youtube/v3/captions";

    /// <summary>
    /// Endpoint URL for updating existing captions.
    /// </summary>
    public static readonly string UpdateEndpoint = "https://www.googleapis.com/upload/youtube/v3/captions";

    /// <summary>
    /// Endpoint URL for downloading captions.
    /// </summary>
    public static readonly string DownloadEndpoint = "https://www.googleapis.com/youtube/v3/captions/";

    /// <summary>
    /// Endpoint URL for deleting captions.
    /// </summary>
    public static readonly string DeleteEndpoint = "https://www.googleapis.com/youtube/v3/captions";
}